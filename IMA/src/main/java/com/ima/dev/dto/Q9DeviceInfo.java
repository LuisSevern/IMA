package main.java.com.ima.dev.dto;

public class Q9DeviceInfo {
	private String serial;
	private String deviceType;
	private String placeId;
	private String status;
	private String woreferprefix;
	private String worefernumber;
	private String creationdate;
	private String lastupdate;
	
	public String getDeviceType() {
		return deviceType;
	}
	public void setDeviceType(String deviceType) {
		this.deviceType = deviceType;
	}
	public String getPlaceId() {
		return placeId;
	}
	public void setPlaceId(String placeId) {
		this.placeId = placeId;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getWoreferprefix() {
		return woreferprefix;
	}
	public void setWoreferprefix(String woreferprefix) {
		this.woreferprefix = woreferprefix;
	}
	public String getWorefernumber() {
		return worefernumber;
	}
	public void setWorefernumber(String worefernumber) {
		this.worefernumber = worefernumber;
	}
	public String getCreationdate() {
		return creationdate;
	}
	public void setCreationdate(String creationdate) {
		this.creationdate = creationdate;
	}
	public String getLastupdate() {
		return lastupdate;
	}
	public void setLastupdate(String lastupdate) {
		this.lastupdate = lastupdate;
	}
	public String getSerial() {
		return serial;
	}
	public void setSerial(String serial) {
		this.serial = serial;
	}

}
